#include "minishell.h"

void	pwd(void)
{
	char	*pwd;
	int		len;

	pwd = getcwd(0, 0);
	len = ft_strlen(pwd);
	write(1, pwd, len);
	write(1, "\n", 1);
	free(pwd);
}
